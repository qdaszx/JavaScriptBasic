/**
 * Global 오브젝트 개요
 * - 모든 <script>를 통해 하나만 존재
 *    - new 연산자로 인스턴스 생성 불가
 *    - 모든 코드에서 공유
 * - 이름(Global)은 있지만
 *    - 오브젝트 실체가 없음
 *    - 오브젝트를 작성(사용)할 수 없음
 */

/**
 * Global 오브젝트 함수, 변수
 * - Global 오브젝트의 함수, 변수를
 *    - Global 함수, Global 변수라고 부름
 * - 함수 안에 작성한 것
 *    - 지역 함수, 로컬 함수라고 부름
 *    - 지역 변수, 로컬 변수라고 부름
 * - 전역 객체라고 부르기도 하지만
 *    - Global은 오브젝트 이름
 *    - 강좌에서는 Global(글로벌) 오브젝트로 표기
 */